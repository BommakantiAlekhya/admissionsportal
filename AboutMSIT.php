                        <p align="justify">MSIT aims to produce skilled Professionals who have knowledge of the core principles underlying information technology and an understanding of emerging industry trends.The MSIT program was started in 2001 and students were admitted to the Learning Centers at International Institute of Information Technology (IIIT), Hyderabad, School of IT, Jawaharlal Nehru Technological University (JNTU), Hyderabad, College of Engineering, Osmania University and Regional Engineering College (REC, now NIT), Warangal.  </p><br>
<p align="justify">The MSIT (MS in IT or Master of Science in Information Technology) is a two year post graduate programme in Computer Science. MSIT is an innovative multi-university interdisciplinary post-graduate programme. MSIT program is being offered by “Consortium of Institutions of Higher Learning” (CIHL), formed by the Universities.<br>
In 2004, the Story/Problem Centered Curriculum was introduced and the fourth batch of students were admitted to the MSIT division, International Institute of Information Technology (IIIT), College of Engineering, Osmania University (OU), Hyderabad and School of Information Technology (SIT), Jawaharlal Nehru Technological University (JNTU).<br>
MSIT offers the latest domain specializations on par with international universities. Computer Networks and  Information Security as  domain specializations are offered for the first time in the states (AP & Telangana) only in MSIT. Besides these, the course also offers specializations in  Software Engineering and E-commerce which have very high demand in the current software industry.
</p>
                      
