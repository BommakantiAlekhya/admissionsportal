               <p class="mb30" align="justify">GAT is the acronym for Graduate Aptitude Test. A good score in GAT is a part-
requirement for admission to The Master of Science in Information Technology
(MSIT) Program, which is a two year post graduate program in Computer
Science. MSIT program is offered by “Consortium of Institutions of Higher
Learning” (CIHL), formed by the Universities. MSIT is currently offered at
International Institute of Information Technology (IIIT-H), Hyderabad, School of
IT, Jawaharlal Nehru Technological University Hyderabad (JNTUH), College of
Engineering, Jawaharlal Nehru Technological University Kakinada (JNTUK) and
College of Engineering, Jawaharlal Nehru Technological University Anantapur
(JNTUA) and Sri Venkateswara University Tirupati (SVU).<br>

GAT is typically taken by Graduates in Engineering of any branch (B.Tech/B.E)
or final year students. GAT measures Literacy, Quantitative and Writing skills that are needed for academic
success in higher learning. GAT assesses problem – solving skills under a strict
time limit. GAT is administered online and is a Computer-based Test. GAT has
been developed, administered and scored by Eduquity Career Technologies (P)
Ltd, a Bangalore-based Human Resources Assessment Company.
               </p>
               <h2 class="post-title" style="color:#ce0058">STRUCTURE OF GAT</h2>
               <p class="mb30" align="justify">GAT consists of two main sections, namely, Critical Reading and, Quantitative
Ability.The Critical Reading
section comprises of Reading Comprehension , Analytical and Logical Reasoning.The Quantitative Ability section
comprises of Discrete Comparisons (Percentages, Ratios, Profit and Loss etc.),
Data Analysis (Graphs, Charts and Tables), Quantitative Comparison (Diagrams,
Formulae, Distance, Time etc.), Sets, Relations and Functions (Venn diagrams,
Linear equations, Intersections, etc.).
               </p>
               <br>
               <h2 class="post-title" style="color:#ce0058">SCORING OF GAT</h2>
               <p class="mb30" align="justify"> Scaled scores are a standardized converted score based on your correct responses. A Scaled Score is the total number of correct answers (i.e, the raw score) that has been converted into a consistent and standardized scale.  In each GAT section, the scores are graded on a scale of 200-800 making a total of 1600 for both the sections.     
               </p>
               <br>  

