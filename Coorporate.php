                        <p class="mb30" align="justify">The proof of concept of any program lies in industry acceptance. MSIT has a strong tie up with the software Industry to understand the pulse of the market and be prepared to beat the competition. Based on inputs from the stalwarts of the IT market the curriculum is enriched and tailored to be suitable for the ever changing needs and pressing demands of the industry. This close participation and collaboration with the industry creates a win-win situation for both the industry and the MSIT Program. The MSITians get the best of the courses based on technologies that are likely to be prevalent when these students hit the market and also benefit from the interactions with the bigwigs like the CEO and VPs of companies like GATI which come to visit MSIT and share their experiences with the students about how the industry runs and what it needs. On the other hand the industry gets 'Ready to work' students who understand and can translate the ideas of the CEO into path breaking software solutions. Apart from technical competency the industry looks for people who possess good softskills. Again MSIT fits the bill because of its unique soft skills program that is delivered with the support of C&K Management Limited.
<br>
