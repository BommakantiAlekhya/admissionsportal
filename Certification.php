 <h2 class="post-title" style="color:#ce0058">IIIT - Hyderabad</h2>
<p class="mb30" align="justify">IIIT, Hyderabad has been established in 1998. Ever since its inception it has achieved remarkable goals and contributed significantly to R&D. IIIT has a sprawling campus of 62 acres and is located in Gachibowli. Some of the most renowned seats of learning like ISB, HCU are located in a radius of 4 km from IIIT.

IIIT has various tie-ups with national and internationals institutes. These include Carnegie Mellon University, Massachusetts Institute of Technology, University of Pennsylvania, University of Washington to name a few.

In IIIT there are various research centers that are autonomous, yet working in concert with each other. The major research centers at IIIT are in the areas of Bio Informatics, Computer Vision, Data Engineering, Language Technologies and Robotics.

MSIT programme was started in IIIT in 2001 and batches were inducted in 2002 - 2004. <br>

visit us at : <a href="http://www.iiit.net" target="_blank" class="blink">http://www.iiit.net</a>
<h2 class="post-title" style="color:#ce0058">JNTU - Hyderabad</h2>
<p class="mb30" align="justify">Under a broad policy framework of providing new technological education required for the industrial growth of the country in general and more particularly for the State of Andhra Pradesh, "Jawaharlal Nehru Technological University" was established on 2nd October, 1972, by an act of State Legislature. On its formation, the Government Engineering Colleges at Anantapur, Kakinada and Hyderabad, became its Constituent Colleges. The first two colleges were established in 1946 and last one in 1965.

MSIT Programme is offered in the School of IT in the University campus in Kukatpally, Hyderabad. This programme was started in JNTU in 2001. MSIT center in the School of IT has state of the art Infrastructure. The Lab is open 24x7x365 with a 2 MBPS Internet Connection and the labs are designed to provide an environment that matches the very best in the field. There are labs exclusively earmarked for MSIT students at JNTU.<br>

visit us at : <a href="http://www.jntuh.ac.in" target="_blank" class="blink">http://www.jntuh.ac.in</a>
 
